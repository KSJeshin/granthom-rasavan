def main():
    print()
    print("Granthom Rasavan — Layout Generation Pipeline")
    print("=" * 50)

    # Verify input files exist
    for path in [LAYOUT_JSON, FREQ_CSV, EFFORT_CSV]:
        if not path.exists():
            print(f"ERROR: File not found: {path}")
            sys.exit(1)

    print(f"  Source     : {LAYOUT_JSON.name}")
    print(f"  Frequencies: {FREQ_CSV.name}")
    print(f"  Effort     : {EFFORT_CSV.name}")

    layout = load_layout(LAYOUT_JSON)
    freq   = load_frequencies(FREQ_CSV)
    effort = load_effort_scores(EFFORT_CSV)
    keys   = extract_keys(layout)

    print(f"  Keys found : {len(keys)} character keys")

    print("\n[1] Computing Weighted Effort Score ...")
    wes = compute_wes(keys, freq, effort)
    if wes:
        print(f"  WES = {wes:.4f}")
    else:
        print("  WES = could not compute (check frequency / effort data)")

    print("\n[2] Generating key_assignment.csv ...")
    generate_key_csv(keys, freq, effort, KEY_CSV, wes)

    print("\n[3] Generating XKB symbols file ...")
    generate_xkb(keys, XKB_OUT)

    print()
    print("Done.")
    print("Generated files are marked AUTO-GENERATED.")
    print("Edit layout JSON → re-run this script → commit JSON + generated files.")

if __name__ == "__main__":
    main()